export function getStationById(id: string): MetroStation | undefined {
  return MUMBAI_METRO_STATIONS.find((s) => s.id === id);
}

export function getStationsByLine(line: 'Line 1' | 'Line 2A' | 'Line 7'): MetroStation[] {
  return MUMBAI_METRO_STATIONS.filter((s) => s.line === line);
}

export function getStationByName(name: string): MetroStation | undefined {
  return MUMBAI_METRO_STATIONS.find((s) => s.name.toLowerCase() === name.toLowerCase());
}

/**
 * Uses Haversine distance to find the nearest station
 */
export function getNearestStation(lat: number, lng: number): MetroStation {
  let nearest = MUMBAI_METRO_STATIONS[0];
  let minDistance = calculateDistance(lat, lng, nearest.lat, nearest.lng);

  for (let i = 1; i < MUMBAI_METRO_STATIONS.length; i++) {
    const d = calculateDistance(lat, lng, MUMBAI_METRO_STATIONS[i].lat, MUMBAI_METRO_STATIONS[i].lng);
    if (d < minDistance) {
      minDistance = d;
      nearest = MUMBAI_METRO_STATIONS[i];
    }
  }

  return nearest;
}

function calculateDistance(lat1: number, lng1: number, lat2: number, lng2: number): number {
  const R = 6371e3; // metres
  const φ1 = (lat1 * Math.PI) / 180;
  const φ2 = (lat2 * Math.PI) / 180;
  const Δφ = ((lat2 - lat1) * Math.PI) / 180;
  const Δλ = ((lng2 - lng1) * Math.PI) / 180;

  const a =
    Math.sin(Δφ / 2) * Math.sin(Δφ / 2) +
    Math.cos(φ1) * Math.cos(φ2) * Math.sin(Δλ / 2) * Math.sin(Δλ / 2);
  const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a));

  return R * c;
}
